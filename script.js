function openNav() {
	document.getElementById("sidenav").style.width = "200px";
	document.getElementById("sidenav").style.paddingLeft = "10px";
	document.getElementById("sidenav").style.paddingRight = "10px";
}

function closeNav() {
	document.getElementById("sidenav").style.width = "0px";
	document.getElementById("sidenav").style.paddingLeft = "0px";
	document.getElementById("sidenav").style.paddingRight = "0px";
}